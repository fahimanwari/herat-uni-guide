async def seed():
    async with SessionLocal() as db:
        # Create main Kankor exam (160 questions format)
        exam = Exam(
            id=uuid.uuid4(),
            title_fa="امتحان نمونه کانکور — جامع",
            title_en="Kankor Mock Exam — Comprehensive",
            description_fa="سوالات نمونه کانکور بر اساس فرمت واقعی امتحان ۱۳۹۸-۱۴۰۵. شامل ریاضی، فزیک، کیمیا، بیولوژی، دری و علوم اسلامی.",
            category="kankor",
            duration_minutes=160,
            total_questions=0,
            is_active=True,
        )
        db.add(exam)
        await db.flush()

        total = 0
        for subject, questions in REAL_QUESTIONS.items():
            for i, (q_text, options, correct_idx) in enumerate(questions):
                question = ExamQuestion(
                    id=uuid.uuid4(),
                    exam_id=exam.id,
                    question_fa=q_text,
                    sort_order=total,
                    points=1,
                )
                db.add(question)
                await db.flush()

                for j, opt_text in enumerate(options):
                    db.add(ExamOption(
                        id=uuid.uuid4(),
                        question_id=question.id,
                        text_fa=opt_text,
                        is_correct=(j == correct_idx),
                    ))
                total += 1

        # Update total questions count
        exam.total_questions = total
        await db.commit()

        print(f"✅ Kankor Mock Exam created:")
        print(f"   Total questions: {total}")
        print(f"   Subjects: Math({len(REAL_QUESTIONS['math'])}), "
              f"Physics({len(REAL_QUESTIONS['physics'])}), "
              f"Chemistry({len(REAL_QUESTIONS['chemistry'])}), "
              f"Biology({len(REAL_QUESTIONS['biology'])}), "
              f"Dari({len(REAL_QUESTIONS['dari'])}), "
              f"Islamic({len(REAL_QUESTIONS['islamic_studies'])})")


if __name__ == "__main__":
    asyncio.run(seed())
